import axios from 'axios';

import { IMessageProcess } from '@/shared/model/message-process.model';

const baseApiUrl = 'api/message-processes';

export default class MessageProcessService {
  public find(id: number): Promise<IMessageProcess> {
    return new Promise<IMessageProcess>((resolve, reject) => {
      axios
        .get(`${baseApiUrl}/${id}`)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public retrieve(): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .get(baseApiUrl)
        .then(res => {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public create(entity: IMessageProcess): Promise<IMessageProcess> {
    return new Promise<IMessageProcess>((resolve, reject) => {
      axios
        .post(`${baseApiUrl}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }
}
